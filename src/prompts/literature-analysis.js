/**
 * 根据论文信息生成分析提示
 */
export function generateAnalysisPrompt(paper) {
  return `请分析以下论文：

**标题**: ${paper.title}
${paper.titleZh ? `**中文标题**: ${paper.titleZh}` : ''}

**摘要 (英文)**:
${paper.abstract || '无摘要'}

${paper.abstractZh ? `**摘要 (中文翻译)**:\n${paper.abstractZh}` : ''}

${paper.keywords?.length ? `**关键词**: ${paper.keywords.slice(0, 5).join(', ')}` : ''}

---
请按照8个模块进行分析：`;
}
